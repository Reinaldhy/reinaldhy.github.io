        <p style="font-size: 100%;" align="justify"><br>Kota Pematangsiantar (sering disingkat Siantar saja) adalah salah satu kota di Provinsi Sumatra Utara. 
        Karena letak Pematangsiantar yang strategis, ia dilintasi oleh Jalan Raya Lintas Sumatra. Kota ini memiliki luas wilayah 79,97 km2 dan berpenduduk sebanyak 247.411 jiwa (2015), dimana Laki-laki berjumlah 120.597 jiwa Dan perempuan 126.814 jiwa.</p><br>
        <p style="font-size: 100%;" align="justify">Kota Pematangsiantar yang hanya berjarak 128 km dari Medan dan 50 km dari Parapat sering menjadi kota perlintasan bagi wisatawan yang hendak ke Danau Toba. Sebagai kota penunjang pariwisata di daerah sekitarnya, kota ini memiliki 8 hotel berbintang, 10 hotel melati dan 268 restoran. Di kota ini masih banyak terdapat sepeda motor BSA model lama sebagai becak bermesin yang menimbulkan bunyi yang keras.</p><br>
        <p style="font-size: 100%;" align="justify">Wakil Presiden Republik Indonesia yang ke-3 Adam Malik, lahir di kota ini pada 22 Juli 1917. Kota ini pernah menerima Piala Adipura pada tahun 1993 atas kebersihan dan kelestarian lingkungan kotanya. Sementara itu, karena ketertiban pengaturan lalu lintasnya, kota ini pun meraih penghargaan Piala Wahana Tata Nugraha pada tahun 1996.</p><br>
        <p style="font-size: 100%;" align="justify">Sektor industri yang menjadi tulang punggung perekonomian kota yang terletak di tengah-tengah Kabupaten Simalungun ini adalah industri besar dan sedang. Dari total kegiatan ekonomi pada tahun 2000 yang mencapai Rp1,69 triliun, pangsa pasar industri mencapai 38,18% atau Rp646 miliar. Sektor perdagangan, hotel dan restoran menyusul di urutan kedua, dengan sumbangan 22,77% atau Rp385 miliar.</p><br>
